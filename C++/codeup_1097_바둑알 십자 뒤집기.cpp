#include<iostream>
using namespace std;

int main()
{
    int n;
    int x, y;
    int arr[19][19];

    for(int i = 0; i < 19; i++)
    {
        for(int j = 0; j < 19; j++)
        {
            cin >> arr[i][j];
        }
    }

    cin >> n;


    for(int i = 0; i < n; i++)
    {

        cin >> x >> y;

            for(int j = 0; j < 19; j++)
            {   
                if(arr[x - 1][j] == 1)
                    arr[x - 1][j] = 0;

                else
                    arr[x - 1][j] = 1;         
            }

            for(int k = 0; k < 19; k++)
            {   
                if(arr[k][y - 1] == 1)
                    arr[k][y - 1] = 0;

                else
                    arr[k][y - 1] = 1;         
            }
    }


for(int i = 0; i < 19; i++)
{
    for(int j = 0; j < 19; j++)
    {
        cout << arr[i][j] << " ";
    }

    cout << endl;
}


    return 0;
}

